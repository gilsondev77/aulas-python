# Pedir ao usuário para digitar a largura e altura
largura = float(input('Digite a largura do retângulo: '))
altura = float(input('Digite a altura do retângulo: '))

# Calcular a área
area_do_retangulo = largura * altura

# Exibir o resultado
print(f'A área do retângulo é: {area_do_retangulo} unidades quadradas.')



