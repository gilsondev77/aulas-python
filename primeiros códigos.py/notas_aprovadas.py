# Solicita a primeira nota do aluno
nota1 = float(input("Digite a primeira nota: "))

# Solicita a segunda nota do aluno
nota2 = float(input("Digite a segunda nota: "))

# Verifica se ambas as notas são maiores ou iguais a 6
if nota1 >= 6 and nota2 >= 6:
    print("Ambas as notas são maiores ou iguais a 6")

else:
    print("Pelo menos uma das notas é menor que 6")



