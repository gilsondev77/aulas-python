# Dicion치rio inicial
dicionario = {
    'carro': 'branco'
}

# Entrada de nova chave e valor
chave = input("Digite a chave que deseja adicionar ou atualizar: ")
valor = input("Digite o valor para essa chave: ")

# Adicionando ou atualizando no dicion치rio
dicionario[chave] = valor

# Exibindo o dicion치rio atualizado
print("Dicion치rio atualizado:")
print(dicionario)
