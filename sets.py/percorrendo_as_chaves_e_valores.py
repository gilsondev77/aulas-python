dicionario = {
    'Nome': 'Gil',
    'Idade': '33',
    'Cidade': 'São Paulo'
}

# Exibindo apenas as chaves
print("Chaves do dicionário:")
for chave in dicionario.keys():
    print(chave)

# Exibindo apenas os valores
print("\nValores do dicionário:")
for valor in dicionario.values():
    print(valor)

