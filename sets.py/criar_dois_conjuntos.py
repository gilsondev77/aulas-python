# Criando o conjunto A
A = {"maçã", "banana", "laranja"}

# Criando o conjunto B
B = {"uva", "banana", "morango"}

# Realizando a união dos conjuntos A e B
uniao = A.union(B)

# Imprimindo os conjuntos e o resultado da união
print("Conjunto A:", A)
print("Conjunto B:", B)
print("União dos conjuntos A e B:", uniao)
